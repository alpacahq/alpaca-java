  /**
   * Wires {@code credentials} into the auth entries of an existing Data {@code ApiClient}.
   * Package-private so tests can supply a hand-crafted authentication map.
   */
  static void applyDataAuth(
      AlpacaCredentials credentials,
      Map<String, markets.alpaca.client.openapi.data.http.auth.Authentication> auths) {
    if (!(auths.get("apiKey")
        instanceof markets.alpaca.client.openapi.data.http.auth.ApiKeyAuth keyAuth))
      throw new IllegalStateException(
          "Expected 'apiKey' ApiKeyAuth in generated Data ApiClient — run ./gradlew generateApis");
    keyAuth.setApiKey(credentials.apiKeyId());
    if (!(auths.get("apiSecret")
        instanceof markets.alpaca.client.openapi.data.http.auth.ApiKeyAuth secretAuth))
      throw new IllegalStateException(
          "Expected 'apiSecret' ApiKeyAuth in generated Data ApiClient — run ./gradlew generateApis");
    secretAuth.setApiKey(credentials.apiSecretKey());
  }

  /**
   * Wires {@code credentials} into the auth entries of an existing Trading {@code ApiClient}.
   * Package-private so tests can supply a hand-crafted authentication map.
   */
  static void applyTradingAuth(
      AlpacaCredentials credentials,
      Map<String, markets.alpaca.client.openapi.trading.http.auth.Authentication> auths) {
    if (!(auths.get("API_Key")
        instanceof markets.alpaca.client.openapi.trading.http.auth.ApiKeyAuth keyAuth))
      throw new IllegalStateException(
          "Expected 'API_Key' ApiKeyAuth in generated Trading ApiClient — run ./gradlew generateApis");
    keyAuth.setApiKey(credentials.apiKeyId());
    if (!(auths.get("API_Secret")
        instanceof markets.alpaca.client.openapi.trading.http.auth.ApiKeyAuth secretAuth))
      throw new IllegalStateException(
          "Expected 'API_Secret' ApiKeyAuth in generated Trading ApiClient — run ./gradlew generateApis");
    secretAuth.setApiKey(credentials.apiSecretKey());
  }

