  private static String streamUrl(String baseUrl, String path) {
    return normalizeBaseUrl(baseUrl, "baseUrl") + "/" + path;
  }

  private static String normalizeBaseUrl(String baseUrl, String name) {
    if (baseUrl == null) throw new NullPointerException(name + " must not be null");
    String trimmed = baseUrl.trim();
    if (trimmed.isEmpty()) throw new IllegalArgumentException(name + " must not be blank");
    while (trimmed.endsWith("/")) {
      trimmed = trimmed.substring(0, trimmed.length() - 1);
    }
    if (trimmed.isEmpty()) throw new IllegalArgumentException(name + " must not be blank");
    return trimmed;
  }
